                                <h1 class="sn-title">Bình Dương triệt phá đường dây mua bán trẻ sơ sinh</h1>
                                <h4>
                                    Qua lời khai của những đối tượng bị bắt, đã có 31 vụ mua bán trẻ sơ sinh thực hiện trót lọt.
                                </h4>
                                <p>
                                    Công an tỉnh Bình Dương kết hợp với nhiều đơn vị vừa triệt phá đường dây mua bán trẻ em dưới 16 tuổi với quy mô lớn. 
                                    Qua quá trình điều tra, cơ quan công an đã bắt giữ nhiều người liên quan đến đường dây này.
                                </p>
                                <p>
                                    Hiện cơ quan điều tra đã khởi tố vụ án: "Mua bán người dưới 16 tuổi'' và "Làm giả giấy tờ của cơ quan tổ chức", khởi tố các bị can để tiếp tục điều tra làm rõ.
                                </p>
                                <p>
                                    Qua lời khai của những đối tượng bị bắt, đã có 31 vụ mua bán trẻ sơ sinh thực hiện trót lọt. 
                                    Tuy nhiên, do vụ án phức tạp liên quan đến nhiều tỉnh thành trong cả nước, những người mua và người bán đều giấu thông tin nên đến nay 
                                    cơ quan công an mới xác định được 7 vụ (7 bé sơ sinh bị bán).
                                </p>
                                <p>
                                    Trong 7 bé sơ sinh mà cơ quan công an xác định được, thì 6 bé đang được các gia đình nuôi dưỡng, một bé thời điểm bắt quả tang đang được gửi vào 
                                    cơ sở bảo trợ xã hội tạm thời chăm sóc.
                                </p>
                                <p>
                                    Đại tá Trần Văn Chính, Phó Giám đốc Công an tỉnh Bình Dương cho biết, đường dây mua bán trẻ sơ sinh này thực chất là những người môi giới kết nối những người không muốn nuôi con 
                                    và những người mua hiếm muộn hoặc có lý do khác để mua bán sang tay kiếm lời.
                                </p>
                                <p>
                                    Những người môi giới mua mỗi bé với giá từ 20-30 triệu đồng rồi bán lại với giá từ 40-50 triệu đồng, đồng thời nhận làm giấy chứng sinh và các giấy tờ khác để hợp thức hóa hành vi mua bán.
                                </p>
                                <p>Nguồn: <a href="https://vtv.vn/phap-luat/binh-duong-triet-pha-duong-day-mua-ban-tre-so-sinh-20221101191725551.htm">VTV.vn</a></p>
